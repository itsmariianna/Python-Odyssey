# 5. Ի՞նչ է լիտերալը (literals) և ի՞նչ տեսակներ գոյություն ունեն։ Հիմնավորել պատասխանը։

# Literal-ների միջոցով մենք արժեքներ ենք ներկայացնում կոդում։ Տվյալներ արտահայտելու միջոց են հանդիսանում։
# դրանք վերագրվում են փոփոխականներին և դրանց միջոցով փոխանցվում են օրինակ ֆունկցիաներին։


# Numeric Literals - 5, 6, 3.14, 8 + 1j

# String Literals - 'hello', "Python"

# Boolean Literals- True, False

# None Literal - None

# Collection Literals - [1, 2, 3], (1, 2, 3), {"key1":"value1", "key2":"value2"}